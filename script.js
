document.addEventListener('DOMContentLoaded', function() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) target.scrollIntoView({ behavior: 'smooth', block: 'start' });
        });
    });

    const clientForm = document.getElementById('clientForm');
    if (clientForm) {
        clientForm.addEventListener('submit', function(e) {
            e.preventDefault();
            const formData = { tipo: 'cliente', nombre: this.querySelector('input[placeholder="Nombre completo"]').value, email: this.querySelector('input[type="email"]').value, telefono: this.querySelector('input[type="tel"]').value, pais: this.querySelector('select').value, fecha: new Date().toISOString() };
            let usuarios = JSON.parse(localStorage.getItem('flixli_usuarios') || '[]');
            usuarios.push(formData);
            localStorage.setItem('flixli_usuarios', JSON.stringify(usuarios));
            alert('¡Registro exitoso! Bienvenido a Flixli.'); this.reset();
        });
    }

    const providerForm = document.getElementById('providerForm');
    if (providerForm) {
        providerForm.addEventListener('submit', function(e) {
            e.preventDefault();
            const selects = this.querySelectorAll('select');
            const formData = { tipo: 'proveedor', nombre: this.querySelector('input[placeholder="Nombre completo"]').value, email: this.querySelector('input[type="email"]').value, telefono: this.querySelector('input[type="tel"]').value, servicio: selects[0].value, pais: selects[1].value, ciudad: this.querySelector('input[placeholder="Ciudad"]').value, fecha: new Date().toISOString() };
            let usuarios = JSON.parse(localStorage.getItem('flixli_usuarios') || '[]');
            usuarios.push(formData);
            localStorage.setItem('flixli_usuarios', JSON.stringify(usuarios));
            alert('¡Registro exitoso como proveedor! Pronto validaremos tu cuenta.'); this.reset();
        });
    }

    document.querySelectorAll('.btn-membership').forEach(btn => {
        btn.addEventListener('click', function() {
            const plan = this.closest('.membership-card').querySelector('h3').textContent;
            if (plan === 'Diamante') alert('¡Felicidades! Has seleccionado el plan Diamante Gratis. Nuestro equipo te contactará para activarlo.');
            else alert(`Has seleccionado el plan ${plan}. Serás redirigido a la pasarela de pagos.`);
        });
    });

    const observer = new IntersectionObserver(function(entries) {
        entries.forEach(entry => { if (entry.isIntersecting) { entry.target.style.opacity = '1'; entry.target.style.transform = 'translateY(0)'; } });
    }, { threshold: 0.1, rootMargin: "0px 0px -50px 0px" });

    document.querySelectorAll('.service-card, .step, .membership-card').forEach(el => {
        el.style.opacity = '0'; el.style.transform = 'translateY(30px)'; el.style.transition = 'all 0.6s ease-out'; observer.observe(el);
    });

    console.log('Flixli App cargada correctamente');
});
